class przelicznik_walutowy:
    stawki = {} 
    def __init__(self, Kursy_walutowe):
        data = (Kursy_walutowe)
        self.stawki = data["price"] 
    
    def konwersja(self, z_kad, do, suma):
        kwota_poczatkowa = suma
        if z_kad != 'EUR' :
            suma = suma / self.stawki[z_kad]
        suma = round(suma * self.stawki[do], 2)
        print('{} {} = {} {}'.format(kwota_poczatkowa, z_kad, suma, do))

